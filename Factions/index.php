<p><b>Faction Spell & Upgrades</b></p>
<br/>
<p>Each Faction has its own Faction specific spell and 3x3 Tier upgrades (3x4 for Mercenaries). The Tier upgrades only show up in order , so when you buy Tier 1 then Tier 2 will show up and then Tier 3, and hence cannot be bought before.</p>
<p>Each Faction also has its own symbol and color code. In the example image below are the yellow Titan Faction upgrades. </p>
<p>In the first column are the Tier 1 upgrades, in the second, Tier 2 and third, Tier 3 upgrades. </p>
<p>The first colored upgrade of each tier, here in yellow, cost Faction coins and they unlock the 3 following black upgrades.</p>
<p>These black upgrades are exclusive faction upgrades and cost usual coins.</p>
<p>Early game, it is advised to quickly affiliate with each Faction at least once, because for each Faction, purchasing all their upgrades once grants their <a href="http://musicfamily.org/realm/FactionChampion">Faction Champion trophy</a>, which in turn rewards their exclusive and universal Heritage upgrade.</p>
<p>Also, the quantity of bought faction upgrades is tracked throughout reincarnations and it is required to unlock Bloodline upgrades.</p>
<p>PS: In order to simplify notation, instead of using the individual names of each Faction upgrade, we generally identify them numerically. e.g.: Druid (2,3) means: In the 2nd Tier, the third secondary Tier upgrade, belonging to Druid. (which is an upgrade named Shapeshifting)</p>
