<p><b>In-game description</b></p>
<p>Titans were an ancient race of giant creatures with godlike powers and millennial knowledge.</p>
<p>Affiliating yourself with the Titans faction means big numbers: they will boost every aspect of your economy to unimaginable values</p>
<p>Titan is the first Neutral Faction to be unlocked as the requirements are the lowest. Their Faction spell is based on luck and can become very powerful. You might want to check out the lightning strike <a target="_blank" href="http://dox4242.github.io/rws/">forecast</a> when playing with their spell.</p>
<p><b>Alignment</b>: Neutral</p>
<p><b>Faction Spell</b>: Lightning Strike</p>
<p><b>Effect</b>: The production of a random building is multiplied by 300% for each Iron Stronghold.</p>
<p><b>Effect</b>: Also grants Faction Coins depending on your current amount of gems.</p>
<p><b>Formula</b>: round(1.5 * ln(1 + x) ^ 3), where x is your current gems.</p>
<br/>
<p><b>Spell Trophy & Upgrade</b>: <img src="http://musicfamily.org/realm/Factions/picks/LightningStormFactionUpgrade.png" alt="Smiley face" align="middle"> <b>Lightning Storm</b></p>
<p><b>Effect</b>: Increase Lightning Strike multiplier to 5x Iron Strongholds.</p>
<p><b>Requirement</b>: Cast Lightning Strike 100 times in a single game.</p>
