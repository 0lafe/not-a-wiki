<p><b>In-game description</b>
<p>When a mercenary crosses your path, there's only one thing that can save your life: offer him more money than he can get by killing you. If you gain the favor of the mercenaries, you will be able to combine the power of all races at once.</p>
<p><b>Alignment</b>: Any</p>
<p><b>Unlock Requirements</b>: R3 - See below</p>
<p><b>Faction Spell</b>: Any (depending on alignment)</p>
<p>Available in all alignments, The Mercenaries can combine the power of the existing factions by letting you choose 2 spells and 3x4 faction upgrades. This enables you to create a custom cross-faction build.</p>
<p>Unlocking the Mercenaries <b><u>Requires 3 reincarnations</u></b> and can be done in different game plays.</p>
<p><b><img src="http://musicfamily.org/realm/Factions/picks/MercenaryTribute.png" alt="Smiley face" align="middle"> Mercenary Tribute</b></p>
<p><b>Cost</b>: At least: 1 Nod (1E60) Coins</p>
<p><b>Cost</b>: 100,000 of each Faction Coin</p>
<p>(Price rises with time and is reset at Abdications.)</p>
<p><b>Coin Formula</b>: 1e60 * (ceil (1+x)/60))^3.5, x is playtime in seconds
<p><b>FC Formula</b>: 100000 * (ceil((1 + x)/60))^0.5, x is playtime in seconds

