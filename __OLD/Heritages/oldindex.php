<p>Each Faction has its own unique and universal Heritage that you can unlock and buy in each subsequent abdication.
<p>To unlock them, you need to unlock the <p><b><a href="http://musicfamily.org/realm/FactionChampion/"> Champion Trophy</a></b> of that faction, which requires buying every Faction Upgrade of that Faction. 
<p>Once unlocked, its price is in Faction Coins, the number depending on the faction's tier: 
<p><b>Vanilla</b>: 5000
<p><b>Neutral</b>: 25000 of two factions
<p><b>Prestige</b>: 75000
<p><b>Neutral Prestige</b>: 400b of Dwarf and Drow
<p><b>Mercenary</b>: 100000 of all.
